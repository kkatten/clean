<p>Эта форма собирает информацию о типе услуги (уборка офисов, мойка фасадов, химчистка ковров и др.), объекте и объёме работ, а также ваше имя и номер телефона. Все предоставленные данные используются исключительно для обработки вашей заявки и подготовки коммерческого предложения и не подлежат передаче третьим лицам.</p>

<h2>Форма для оставления отзыва</h2>
<p>В этой форме вы можете оставить свою оценку, имя и текст сообщения. Ваши отзывы помогают нам улучшать качество услуг. Мы гарантируем, что ваши данные будут обработаны конфиденциально.</p>

