<h2>2. Основные понятия, используемые в Политике</h2>
<p><strong>2.1.</strong> Персональные данные — любая информация, относящаяся к прямо или косвенно определённому физическому лицу.</p>
<p><strong>2.2.</strong> Обработка персональных данных — действия (сбор, запись, систематизация, хранение, уточнение, извлечение, использование, передача, обезличивание, блокирование, удаление, уничтожение) с персональными данными.</p>
<p><strong>2.3.</strong> Оператор — лицо, самостоятельно или совместно с другими организующее и/или осуществляющее обработку персональных данных.</p>

<h2>3. Цели обработки</h2>
<p>Персональные данные обрабатываются в целях связи с пользователем по оставленным заявкам, обратной связи и предоставления информации об услугах.</p>

<h2>4. Правовые основания</h2>
<p>Обработка осуществляется на основании согласия субъекта персональных данных и в случаях, предусмотренных законодательством РФ.</p>

